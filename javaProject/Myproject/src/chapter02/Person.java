package chapter02;

public class Person {

	public static void main(String[] args) {
//		회원이름을 저장하는 변수
		String name = "조상희";
//		회원 전화번호 ( 000-0000-0000 )를 저장하는 변수
		String phone = "000-0000-0000";
//		회원 주민등록번호 ( 000000-0000000 또는 0000000000000 )를 저장하는 변수
		String resiSecNum = "000000-0000000 또는 0000000000000";
		
		System.out.println("이름 : " + name + "\n전화번호 : " + phone + "\n주민등록번호 : " + resiSecNum);
	}

}
